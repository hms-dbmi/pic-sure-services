DROP TABLE IF EXISTS `query_status`;

CREATE TABLE `query_status` (
    QUERY VARCHAR(63) NOT NULL,
    SITE VARCHAR(20) NOT NULL,
    DATE_STARTED DATETIME NOT NULL,
    GENOMIC_STATUS VARCHAR(64) NOT NULL DEFAULT 'NotStarted',
    PHENOTYPIC_STATUS VARCHAR(64) NOT NULL DEFAULT 'NotStarted',
    PRIMARY KEY (`QUERY`)
);
